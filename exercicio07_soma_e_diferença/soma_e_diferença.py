#Ler 3 números, calcular e escrever a soma dos 2 primeiros e a diferença entre os 2 últimos.

numero1 = float(input("Digite o primeiro número: "))
numero2 = float(input("Digite o segundo número: "))
numero3 = float(input("Digite o terceiro número: "))
soma = numero1 + numero2
diferença = numero2 - numero3
print(f"A soma dos dois primeiros números é: {soma}")
print(f"A soma dos dois últimos números é: {diferença}")

