#Ler o valor da base e da altura de um triângulo e calcular a sua área. A fórmula para calcular a área de um triângulo é: Área = (base * altura) / 2.

base = float(input("Digite o valor da base do triângulo: "))
altura = float(input("Digite o valor da altura do triângulo: "))
area = (base * altura) / 2
print("A área do triângulo é:", area)
